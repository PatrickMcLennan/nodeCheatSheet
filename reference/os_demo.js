const os = require('os');

// Get Platform
console.log(os.platform());

// CPU Architecture
console.log(os.arch());

// CPU Core Info
console.log(os.cpus());

// Free Memory
console.log(os.freemem());

// Total Memory
console.log(os.totalmem());

// Home Dir
console.log(os.homedir());

// Uptiime
console.log(os.uptime());
